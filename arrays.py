# Arrays
cars = ["Ford", "Volvo", "BMW"]
x = cars[0]
print(x)

# Length of an Array
x = len(cars)
print(x)

# Looping Array Elements
for x in cars:
  print(x)

# Adding Array Elements
cars = ["Ford", "Volvo", "BMW"]

cars.append("Honda")

print(cars)

# Removing Array Elements

cars = ["Ford", "Volvo", "BMW"]

cars.pop(1)

print(cars)

